                    <h2>When and how often to eat</h2>

                    <p>It's not just about what you eat on a trek, but also when you eat. To stay energised during the day, you should eat on a regular basis. This is especially true if you're eating simple carbohydrates, which provide short spurts of energy that quickly wear off. It's a good idea to aim to eat every two hours to gently and consistently drip carbs into your system, preventing energy spikes and troughs.</p>

                    <p>You should have a large breakfast before starting your hike and refill within one hour of ending. It's also a good idea to eat about half an hour before bedtime to try to replenish the energy you expended on your journey, especially if you plan on hiking the next day. </p>
