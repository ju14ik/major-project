        <h1>The award winning bakery</h1>
        <p>
          Richardson’s traditional Bakery was founded by Andrew Richardson and his wife Mary Richardson in 1991 after their long desire to buy a decent loaf to put on the table. 
        </p>
        <p>
          We are using traditional methods for producing hand–crafted baked goods and using the finest locally sourced ingredients. However, traditional doesn’t mean old–fashioned. We’re innovative bakers, always experimenting with new flavours and combinations, new ways to bring you fresh, exciting and above all, tasty ideas. Our plan was to provide a place for customers to buy a decent loaf which had not gone through numerous processes to reach the table. 
        </p>
        <p>
          Here in the bakery, we’re busy creating things for you to enjoy every day – delicious fresh breads, bagels, pastries and cakes. Beyond ingredient transparency, commitment to the environment, reduction of food waste, dedication to employees, and genuine care for the customer we’ve reduced the packaging from a predominantly plastic to paper-based option. Our products are packaged in Eco-Friendly Bakery Packaging, Eco craft bread bag and cake boxes.
        </p>
