                <h2>How To Make Dehydrated Risotto</h2>

                <div class="cooking-instructions">
                    <div class="time">
                        <ul>
                            <li><span>Prep Time:</span> 5 minutes</li>
                            <li><span>Cook Time:</span> 30 minutes</li>
                            <li><span>Dehydrating Time:</span> 6 hours</li>
                            <li><span>Total Time:</span> 35 minutes</li>
                        </ul>
                    </div>

                    <p class="servings">2 servings</p>

                    <div class="component">
                        <h3>Equipment</h3>
                        <ul>
                            <li>Dehydrator</li>
                            <li>Dehydrator Tray Liner</li>
                            <li>Backpacking Stove</li>
                            <li>Backpacking Pot</li>
                        </ul>
                    </div>
                    
                    <div class="component">
                        <h3>Ingredients</h3>
                        <ul>
                            <li>1 tablespoon oil</li>
                            <li>1 small onion, diced</li>
                            <li>1 cup arborio rice</li>
                            <li>½ cup white wine</li>
                            <li>2 cups broth</li>
                            <li>Salt</li>
                            <li>1 small zucchini</li>
                            <li>6-8 mushrooms</li>
                            <li>¼ cup frozen peas</li>
                            <li>2 tablespoons parmesan cheese</li>
                            <li>2 tablespoons olive oil, packed separately</li>
                        </ul>
                    </div>

                    <div class="instructions">
                        <h3>Instructions</h3>
                        <ol>
                            <li><span>Prepare the risotto: </span>Heat oil in a heavy-bottomed pot. Once shimmering, add the onion and salt and saute 6 minutes. Add the rice and saute 1-2 minutes, until the ends turn translucent. Add the wine and cook, stirring continuously, until it has evaporated. Add the broth and cook, stirring frequently and adding water a half cup at a time as needed when the bottom of the pan is dry, until the rice is tender - about 20 minutes. Remove from heat and let the risotto cool for a bit in the pot.</li>
                            <li>While the risotto is cooling, cut the zucchini and mushrooms into ¼” slices.</li>
                            <li><span>Dehydrate: </span>Places the zucchini, mushrooms, and peas onto your dehydrator trays, taking care to make sure the vegetables don’t overlap. Line an additional dehydrator tray or two (depending on the size) with a mesh or solid tray liner or piece of parchment paper. Spread the risotto onto the tray in an even layer.</li>
                            <li>Dehydrate 4-8 hours at 135F, until the vegetables and risotto are totally dry. (You may want to check the risotto half way and flip it to ensure even drying.) Store in a sealed, airtight container until ready to cook at camp.</li>
                            <li><span>Pack Your Bear Barrel: </span>Pack the risotto, 2 tablespoons cheese, and a small container of olive oil (optional - will add bonus calories to the meal!)</li>
                            <li><span>In camp: </span>Place the dehydrated risotto and veggies in a cookpot along with 2 tablespoons parmesan cheese. Add enough water to just cover - about 2 cups. Optional: Let the food soak for a bit to reduce cooking time & fuel consumption. Bring to a boil and then simmer, stirring frequently, until the risotto and vegetables are tender, adding more water if needed. Enjoy!</li>
                        </ol>
                    </div>

