                <h1>Dehydrated Risotto with Vegetables</h1>

                <p>Creamy risotto rice, hearty mushrooms, and fresh zucchini, this dehydrated risotto is one of our all-time favorite backpacking recipes. Now you can enjoy a warm, comforting bowl of risotto on your next backpacking trip without all time-consuming work!</p>

                <img src="./images/risotto/risotto.jpg" alt="" title=""/>

                <p>We love risotto. For us, it’s top of the list in terms of comfort food. On a rainy day or chilly night, nothing brings us joy quite like the smell of a rich and creamy bowl of risotto. It’s hearty and filling, but still surprisingly bright – especially when cooked with white wine and spring veggies!</p>
                
                <p>We can definitely think of a few nights in the backcountry where a bowl of risotto would have been heavenly. (I’m thinking specifically of a cold and stormy we night we rode out in the Mt. Jefferson Wilderness. Yeesh!)</p>

                <p>But despite its morale-boosting qualities, traditional risotto isn’t all that backpacking friendly. Between the active cooking time over low, controlled heat (something backpacking stoves are notoriously bad at), and the weight of the ingredients, it’s just not the best candidate for lightweight backpacking.</p>

                <img src="./images/risotto/dehydrated-risotto.jpg" alt="" title=""/>

                <p>But, we found the solution… <span>make the risotto at home and dehydrate it!</span> This way you can perform all the time-consuming parts of the recipe in the comfort of your home kitchen. Then, when you’re out in the field, all you have to do is rehydrate in some boiling water and it’s ready to eat!</p>

                <p>And course, if you’re already dehydrating your risotto at home, then you might as well dehydrate some vegetables to go with it, right? For this particular recipe, we went with zucchini, mushrooms, and peas which add some nutritional punch.</p>

                <p>With a little prep work at home, you too can enjoy the rich creaminess of homemade risotto while backpacking in no time at all!</p>

